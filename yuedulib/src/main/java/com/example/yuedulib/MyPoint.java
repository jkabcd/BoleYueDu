package com.example.yuedulib;

public class MyPoint {
    float x,y;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public MyPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public MyPoint() {
    }
}
